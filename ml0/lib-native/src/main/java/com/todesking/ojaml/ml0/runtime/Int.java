package com.todesking.ojaml.ml0.runtime;

public class Int {
  public static int add(int l, int r) { return l + r; }
  public static int sub(int l, int r) { return l - r; }
  public static int mul(int l, int r) { return l * r; }
  public static int div(int l, int r) { return l / r; }
  public static int mod(int l, int r) { return l % r; }
}
